<?php
App::uses('AppController', 'Controller');
/**
 * Users Controller
 *
 * @property User $User
 * @property PaginatorComponent $Paginator
 * @property PaginatorComponent $Paginator
 * @property SessionComponent $Session
 * @property AuthComponent $Auth
 */
class UsersController extends AppController {

/**
 * Helpers
 *
 * @var array
 */
	public $helpers = array('Html', 'Form', 'Session');

/**
 * Components
 *
 * @var array
 */
	public $components = array('Paginator');
        
 /**
 * beforeFilter
 *
 * Tell the AuthComponent to let un-authenticated users access the users add
  * function
 * Letting users register themselves
 * 
 */       
        public function beforeFilter() {
            parent::beforeFilter();
            $this->Auth->allow('add'); 
        }

 /**
 * login
 *
 * Check for Authenticate users from $this->Auth->login() in AuthComponent. 
 * If the user is Authenticate, redirect the users 
 * base on the $this->Auth->redirect() in AuthComponent
 * Url loginRedirect is set in the AppController under Auth in Component
 * 
 * If the user is not Authenticate, display message and stay in the login page
 * 
 */ 
        public function login() {
            if ($this->request->is('post')) {
                if ($this->Auth->login()){
                    return $this->redirect($this->Auth->redirect());
                }
                $this->Session->setFlash(__("Invalid username or password, try again"));
            }
        }

 /**
 * logout
 *
 * Redirect the users base on the $this->Auth->logout() in AuthComponent
 * Url logoutRedirect is set in the AppController under Auth in Component
 * 
 */ 
        public function logout() {
            return $this->redirect($this->Auth->logout());
        }

        /**
 * index method
 *
 * @return void
 */
	public function index() {
		$this->User->recursive = 0;
		$this->set('users', $this->Paginator->paginate());
	}

/**
 * view method
 *
 * @throws NotFoundException
 * @param string $id
 * @return void
 */
	public function view($id = null) {
		if (!$this->User->exists($id)) {
			throw new NotFoundException(__('Invalid user'));
		}
		$options = array('conditions' => array('User.' . $this->User->primaryKey => $id));
		$this->set('user', $this->User->find('first', $options));
	}

/**
 * add method
 *
 * @return void
 */
	public function add() {
		if ($this->request->is('post')) {
			$this->User->create();
			if ($this->User->save($this->request->data)) {
				$this->Session->setFlash(__('The user has been saved.'));
				return $this->redirect(array('action' => 'index'));
			} else {
				$this->Session->setFlash(__('The user could not be saved. Please, try again.'));
			}
		}
	}

/**
 * edit method
 *
 * @throws NotFoundException
 * @param string $id
 * @return void
 */
	public function edit($id = null) {
		if (!$this->User->exists($id)) {
			throw new NotFoundException(__('Invalid user'));
		}
		if ($this->request->is(array('post', 'put'))) {
			if ($this->User->save($this->request->data)) {
				$this->Session->setFlash(__('The user has been saved.'));
				return $this->redirect(array('action' => 'index'));
			} else {
				$this->Session->setFlash(__('The user could not be saved. Please, try again.'));
			}
		} else {
			$options = array('conditions' => array('User.' . $this->User->primaryKey => $id));
			$this->request->data = $this->User->find('first', $options);
		}
	}

/**
 * delete method
 *
 * @throws NotFoundException
 * @param string $id
 * @return void
 */
	public function delete($id = null) {
		$this->User->id = $id;
		if (!$this->User->exists()) {
			throw new NotFoundException(__('Invalid user'));
		}
		$this->request->onlyAllow('post', 'delete');
		if ($this->User->delete()) {
			$this->Session->setFlash(__('The user has been deleted.'));
		} else {
			$this->Session->setFlash(__('The user could not be deleted. Please, try again.'));
		}
		return $this->redirect(array('action' => 'index'));
	}

